package week2.operator;

public class W03 {
    public static void main(String[] args) {

        // 비교 연산자
        System.out.println(10 > 9);
        System.out.println(10 >= 9);
        System.out.println(10 < 9);
        System.out.println(10 <= 9);
        System.out.println(10 == 10);
        System.out.println(10 == 9);
        System.out.println(10 != 10);
        System.out.println(10 != 9);

    }
}

package week2.operator;

public class W10 {
    public static void main(String[] args) {

        // 비트 연산자
        System.out.println(3 << 2); // 3 * (2*2)
        System.out.println(3 << 1); // 3 * 2
        System.out.println(3 >> 2); // 3 / (2*2)
        System.out.println(3 >> 1); // 3 / 2

    }
}
